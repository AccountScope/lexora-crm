-- Function to clean up old rejected suggestions (keep last 30 days)
CREATE OR REPLACE FUNCTION cleanup_old_time_suggestions()
RETURNS void AS $$
BEGIN
  DELETE FROM time_suggestions
  WHERE status = 'rejected'
    AND created_at < NOW() - INTERVAL '30 days';
END;
$$ LANGUAGE plpgsql;

